window.ColorUtils = {
    getColorHex: function(colorName) {
        return window.COLOR_HEX_MAP[colorName] || '#CCCCCC';
    },
    
    getSeasonData: function(seasonName) {
        return window.SEASONS[seasonName] || null;
    },
    
    getConsultationMessage: function(seasonName) {
        return window.CONSULTATION_MESSAGES[seasonName] || null;
    },
    
    getAllSeasons: function() {
        return Object.keys(window.SEASONS);
    },
    
    getSeasonGroup: function(seasonName) {
        const lowerName = seasonName.toLowerCase();
        if (lowerName.includes('spring')) return 'spring';
        if (lowerName.includes('summer')) return 'summer';
        if (lowerName.includes('autumn')) return 'autumn';
        if (lowerName.includes('winter')) return 'winter';
        return 'unknown';
    }
};

